    private void btnClientePesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClientePesquisarActionPerformed
        // TODO add your handling code here:
        long cpfClientePesquisar = Long.parseLong(txtClientePesquisar.getText());
        ClientesDAO clientesDAO = new ClientesDAO();
        Clientes clientes = clientesDAO.getClientes(cpfClientePesquisar);
        if(clientes == null){
            JOptionPane.showMessageDialog(this, "Cliente n√£o encontrado");
        }else{
            txtCpfCliente.setText(Long.toString(clientes.getCpf()));
            txtNomeCliente.setText(clientes.getNome());
        }
    }//GEN-LAST:event_btnClientePesquisarActionPerformed

