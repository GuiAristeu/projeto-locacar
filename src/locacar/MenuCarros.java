    private void btnCarroCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarroCadastrarActionPerformed
        // TODO add your handling code here:
        String marca = txtMarcaCarro.getText();
        String modelo = txtModeloCarro.getText();
        String cor = txtCorCarro.getText();
        int quilometragem = Integer.parseInt(txtKmCarro.getText());
        int valordia = Integer.parseInt(txtValorDiaCarro.getText());
        int valorkm = Integer.parseInt(txtValorKmCarro.getText());
        boolean status = true;
        
        Carros carros = new Carros();
        carros.setMarca(marca);
        carros.setModelo(modelo);
        carros.setCor(cor);
        carros.setQuilometragem(quilometragem);
        carros.setValordia(valordia);
        carros.setValorkm(valorkm);
        carros.setStatus(status);
        
        CarrosDAO carrosDAO = new CarrosDAO();
        carrosDAO.inserir(carros);
        
        txtMarcaCarro.setText("");
        txtModeloCarro.setText("");
        txtCorCarro.setText("");
        txtKmCarro.setText("");
        txtValorKmCarro.setText("");
        txtValorDiaCarro.setText("");
    }//GEN-LAST:event_btnCarroCadastrarActionPerformed

