    private void btnCarroPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarroPesquisarActionPerformed
        // TODO add your handling code here:
        int idPesquisaCarro = Integer.parseInt(txtCarroPesquisar.getText());
        CarrosDAO carrosDAO = new CarrosDAO();
        Carros carros = carrosDAO.getCarros(idPesquisaCarro);
        if(carros == null){
            JOptionPane.showMessageDialog(this, "Carro n√£o encontrado");
        }else{
            txtIdCarro.setText(Integer.toString(carros.getId()));
            txtMarcaCarro.setText(carros.getMarca());
            txtModeloCarro.setText(carros.getModelo());
            txtCorCarro.setText(carros.getCor());
            txtKmCarro.setText(Integer.toString(carros.getQuilometragem()));
            txtValorDiaCarro.setText(Integer.toString(carros.getValordia()));
            txtValorKmCarro.setText(Integer.toString(carros.getValorkm()));
        }
    }//GEN-LAST:event_btnCarroPesquisarActionPerformed

